package samples.demo;

public class Location {
	
	private String area;
	private String state;
	
	public void setArea(String area) {
		this.area = area;
	}
	
	public String getArea() {
		return this.area;
	}
	
	public void setState(String state) {
		this.state = state;
	}
	
	public String getState() {
		return this.state;
	}

}
